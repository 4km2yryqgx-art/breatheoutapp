def current_week_key(dt: datetime.datetime = None) -> str:
    """ISO-неделя вида '2026-W30' — одинакова для всех пользователей одновременно."""
    dt = dt or datetime.datetime.utcnow()
    year, week, _ = dt.isocalendar()
    return f"{year}-W{week:02d}"


def get_challenge_for_week(week_key: str) -> dict:
    """Детерминированно выбирает испытание недели по её ключу — без хранения в БД."""
    index = sum(ord(c) for c in week_key) % len(WEEKLY_CHALLENGES)
    return {"index": index, "title": WEEKLY_CHALLENGES[index], "week_key": week_key}


